package testpackage;

public class ConditionalStatements {

	public static void main(String[] args) {

		int age = 17;

		if (age > 18) {
			System.out.println("Age is greater than 18.So, This guy is eligible to vote");
		} else if (age == 18) {
			System.out.println("Age is equal to 18.So, This guy is eligible to vote");
		} else {
			System.out.println("Age is less than 18.So, This guy is not eligible to vote");
		}
		
		
		

	}

}
